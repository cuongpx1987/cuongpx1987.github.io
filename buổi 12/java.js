function dientich()
{
    var a = parseInt(prompt("Bán kính"));
    var duongkinh = a * 2;
    var chuvi = a * 2 * Math.PI;
    var dientich = a ** 2 * Math.PI;
    document.write ("Đường kính hình tròn là " + duongkinh + " <br> "  );   
    document.write ("Chu vi hình tròn là " + chuvi + " <br>" ); 
    document.write ("Diện tích hình tròn là " + dientich  );   
    
}    dientich();


 